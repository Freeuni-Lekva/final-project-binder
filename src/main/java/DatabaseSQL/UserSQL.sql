Create table User(
                     id INT,
                     name varchar(30) NOT NULL,
                     surname varchar(30) NOT NULL,
                     email varchar(30) NOT NULL,
                     username varchar (30),
                     password varchar(30) NOT NULL,
                     dateOfBirth varchar(30) NOT NULL,
                     age INT,
                     city varchar(30) NOT NULL,
                     hobbies varchar(300)
);