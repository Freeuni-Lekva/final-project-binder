Create table User(
                     id int AUTO_INCREMENT PRIMARY KEY,
                     name varchar(30) ,
                     surname varchar(30) ,
                     email varchar(30) ,
                     username varchar (30),
                     password varchar(30) ,
                     dateOfBirth varchar(30) ,
                     age INT,
                     city varchar(30) ,
                     hobbies varchar(300),
                     gender varchar(30)
);